/*****************************************************************************/
/*                                                                           */
/* egts_header_crc()                                                         */
/*                                                                           */
/* Description: Calculates transport header CRC                              */
/*                                                                           */
/* Arguments: phead - transport header                                       */
/*            pecnoded_part - encoded part of header                         */
/*            encoded_size - encoded part size                               */
/*                                                                           */
/* Return:    CRC                                                            */
/*                                                                           */
/* Other:     function used internally                                       */
/*                                                                           */
/*****************************************************************************/

u8    egts_header_crc( egts_header_t* phead , void* pecnoded_part , u8 encoded_size )
{
  u8 crc = 0xFF, i, *puc;

  crc = crc8_table[ crc ^ phead->PRV ];
  crc = crc8_table[ crc ^ phead->SKID ];
  crc = crc8_table[ crc ^ egts_get_header_bits( phead ) ];
  crc = crc8_table[ crc ^ phead->HL ];
  crc = crc8_table[ crc ^ phead->HE ];
  puc = (u8*)pecnoded_part;
  for ( i = 0U; 
        i < encoded_size; 
        i++ ) {
    crc = crc8_table[ crc ^ *puc++ ];
  }

  return crc;
}

/*****************************************************************************/
/*                                                                           */
/* egts_data_crc()                                                           */
/*                                                                           */
/* Description: Calculates frame data CRC                                    */
/*                                                                           */
/* Arguments: pbuf - frame data                                              */
/*            sz - frame data size                                           */
/*                                                                           */
/* Return:    CRC                                                            */
/*                                                                           */
/* Other:     function used internally                                       */
/*                                                                           */
/*****************************************************************************/

u16   egts_data_crc   ( void* pbuf , u16 sz )
{
  u16 crc = 0xFFFFU;
  u8 *puc = (u8*)pbuf;
  while ( 0U != sz-- ) {
    crc = (u16)(crc << 8) ^ crc16_table[ (u16)(crc >> 8) ^ *puc++ ];
  }
  return crc;
}


